package pers.asx.merge;

/**
 * @author: asx
 * @date: 2023/6/30
 * @descrition:
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
